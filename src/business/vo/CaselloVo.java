package business.vo;

public class CaselloVo {
	private int id;
	private String nome;
	private short chilometro;
	private int idAutostrada;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public short getChilometro() {
		return chilometro;
	}
	public void setChilometro(short chilometro) {
		this.chilometro = chilometro;
	}
	public int getIdAutostrada() {
		return idAutostrada;
	}
	public void setIdAutostrada(int idAutostrada) {
		this.idAutostrada = idAutostrada;
	}
}
